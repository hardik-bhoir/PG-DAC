 // find and print first five numbers
 class PrimeNum{
	public static void main(String args[]){
		int num= 2,j;
		for(int i= 0; i<5; )// for the iterations of prime numbers
		{
			for(j=2; j<num; j++)
			{
				if (num%j==0)
				{
					break;
				}
				
			}
			if(j==num)
			{
				System.out.println(num);
				
			}
			
			num++;// increment the current value of 
			i++;// increment the iteration 
		}
		
	}	
	
 }
		
		

		
		
	 
 
			
			
				
					
				
		
		
		
	

	
	
	
	

	
	
	
	
	
	
